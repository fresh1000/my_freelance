
public class XYZPoint {
	private int X;
	private int Y;
	private int Z;
	
	public XYZPoint(int x, int y, int z) {
		this.X = x;
		this.Y = y;
		this.Z = z;
	}

	public int getX() {
		return this.X;
	}

	public void setX(int x) {
		this.X = x;
	}

	public int getY() {
		return this.Y;
	}

	public void setY(int y) {
		this.Y = y;
	}

	public int getZ() {
		return this.Z;
	}

	public void setZ(int z) {
		this.Z = z;
	}
	
}
